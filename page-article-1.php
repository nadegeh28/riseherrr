            Chaque année, en Belgique, des dizaines de femmes sont tuées simplement parce qu’elles sont des femmes.
            Ces crimes, appelés féminicides, sont l’aboutissement d’un cycle de violences sexistes profondément enracinées 
            dans notre société. Entre 2012 et 2022, plus de 14.000 femmes ont été tuées en Europe, selon les données d’Eurostat. 
            Parmi elles, plus de 4.300 ont perdu la vie aux mains de leur conjoint, et 2.400 autres ont été tuées par des membres 
            de leur famille. En Belgique, cependant, les données officielles sur les victimes d’homicides, y compris leur sexe, 
            font cruellement défaut.
        </p>
        <p>
        Face à ce manque de statistiques, des initiatives citoyennes et associatives comblent le vide. 
        Le blog Stop Féminicide recense minutieusement chaque cas rapporté par la presse, 
        révélant qu’environ 24 à 27 féminicides sont commis chaque année dans le pays, 
        majoritairement par des conjoints ou ex-conjoints. Ce travail essentiel, réalisé souvent 
        dans des conditions difficiles, permet de donner un nom et un visage aux victimes.
        </p>


        <p>
        En 2023, la Belgique a adopté la loi Stop Féminicide, 
        une avancée historique qui reconnaît officiellement le féminicide comme un crime spécifique et définit 
        ses différentes formes : féminicide intime (commis par un partenaire ou ex-partenaire), 
        non intime (par un tiers, comme un client), indirect (résultant de violences comme un avortement forcé), 
        et homicide fondé sur le genre. Cette loi marque une étape importante en posant les bases pour mieux comprendre 
        le phénomène grâce à la collecte de statistiques officielles.
        </p>
        <p>
        Malgré cela, de nombreux défis restent à relever. La loi est avant tout symbolique et manque de financements 
        suffisants pour déployer des mesures concrètes de prévention, comme l’éducation à l’égalité dès le plus jeune âge. 
        Les associations dénoncent également l’absence d’un véritable ministère des Droits des femmes, capable de coordonner 
        les politiques à l’échelle nationale. Les féminicides sont l’aboutissement d’un continuum de violences sexistes, 
        et leur prévention nécessite une remise en question profonde des normes sociétales, notamment celles qui légitiment 
        encore trop souvent la violence 
        </p>

